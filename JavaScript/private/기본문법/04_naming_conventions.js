/**
 *  1) 영어를 사용하면 문자와 숫자를 모두 사용
 *  2) 특수기호는 _ 와 $ 사용
 *  3) 숫자로 이름을 시작할 수 없음
 *      1Name
 *  4) 키워드는 변수명으로 사용 불가능
 */
let $ive = "아이브";
const _yujin = `안유진`;

console.log($ive);
console.log(_yujin);
/**
 * Naming Convention 2
 * 1)camleCase - 대부분의 언어에서 사용 첫번째글자 그다음 문자의 대문자로 쓰는 거
 * 2)snake_case - 단어마다 언더스코어로 묶음
 * 3)PascalCase - 카멜케이스와 같은데 첫글자도 대문자를씀
 */

const anYujin = "안유진";
console.log(anYujin);

let ahn = "아아아ㅏㅇ";
console.log("ahn", ahn);
